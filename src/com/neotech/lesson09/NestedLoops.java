package com.neotech.lesson09;

public class NestedLoops 
{
	public static void main(String[] args) 
	{
		
		for(int i = 1; i <= 5; i++)
		{
		//	System.out.println("i: " + i);
			
			for (int j = 1; j <= 5; j++)
			{ //in this area now I have two variables(indexes) i and j
				
				
			//	System.out.print(" i : "  + i + " j: "  + j);
			
				System.out.print(i);
			}
			
			
			System.out.println();
		}
		
	}
}
